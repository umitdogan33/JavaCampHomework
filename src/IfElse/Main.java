package IfElse;

public class Main {
    public static void main(String[] args) {
        int sayi = 20;

        if (sayi < 20) {
            System.out.println("20'den küçük");
        } else if (sayi == 20) {
            System.out.println("20'ye eşit");
        }
        else {
            System.out.println("20'den büyük");
        }
    }
}
